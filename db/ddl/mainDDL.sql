CREATE OR REPLACE TRIGGER TRG_ORDER_APPR_AI
    AFTER INSERT
    ON ORDER_APPROVAL
    FOR EACH ROW
DECLARE
    V_APPROVAL_ROLE_ID EMPLOYEE.ROLE_ID%TYPE;
BEGIN
    SELECT ROLE_ID
    INTO V_APPROVAL_ROLE_ID
    FROM EMPLOYEE
    WHERE EMPLOYEE_ID = :NEW.APPROVAL_EMPLOYEE_ID;

    IF :NEW.APPROVAL_STATUS = 'APPROVED' THEN
        UPDATE ORDER_REQUEST
        SET ORDER_STATUS         = 'APPROVED',
            APPROVAL_EMPLOYEE_ID = :NEW.APPROVAL_EMPLOYEE_ID,
            APPROVAL_ROLE_ID     = V_APPROVAL_ROLE_ID,
            APPROVED_QUANTITY    = :NEW.APPROVED_QUANTITY,
            REJECT_REASON        = NULL,
            APPROVED_AT          = SYSDATE,
            REJECTED_AT          = NULL
        WHERE ORDER_REQUEST_ID = :NEW.ORDER_REQUEST_ID
          AND ORDER_STATUS = 'REQUESTED';
    ELSIF :NEW.APPROVAL_STATUS = 'REJECTED' THEN
        UPDATE ORDER_REQUEST
        SET ORDER_STATUS         = 'REJECTED',
            APPROVAL_EMPLOYEE_ID = :NEW.APPROVAL_EMPLOYEE_ID,
            APPROVAL_ROLE_ID     = V_APPROVAL_ROLE_ID,
            APPROVED_QUANTITY    = NULL,
            REJECT_REASON        = :NEW.APPROVAL_COMMENT,
            APPROVED_AT          = NULL,
            REJECTED_AT          = SYSDATE
        WHERE ORDER_REQUEST_ID = :NEW.ORDER_REQUEST_ID
          AND ORDER_STATUS = 'REQUESTED';
    END IF;
END;
/

CREATE OR REPLACE TRIGGER TRG_STORE_RCPT_AI
    AFTER INSERT
    ON STORE_RECEIPT
    FOR EACH ROW
BEGIN
    IF :NEW.RECEIPT_STATUS = 'CANCELED' THEN
        UPDATE ORDER_REQUEST
        SET ORDER_STATUS = 'CANCELED'
        WHERE ORDER_REQUEST_ID = :NEW.ORDER_REQUEST_ID;
    ELSE
        UPDATE ORDER_REQUEST
        SET ORDER_STATUS = 'DONE'
        WHERE ORDER_REQUEST_ID = :NEW.ORDER_REQUEST_ID;
    END IF;
END;
